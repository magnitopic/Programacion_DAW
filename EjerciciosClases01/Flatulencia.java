package EjerciciosClases01;

public class Flatulencia {
	private String olor;
	private int ruido;

	public Flatulencia(String olor, int ruido) {
		this.olor = olor;
		this.ruido = ruido;
	}

	public String getOlor() {
		return this.olor;
	}

	public void setOlor(String olor) {
		this.olor = olor;
	}

	public int getRuido() {
		return this.ruido;
	}

	public void setRuido(int ruido) {
		this.ruido = ruido;
	}

}
